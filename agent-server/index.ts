type Provider = 'claude' | 'copilot';

interface IncomingMessage {
  type: 'send' | 'stop' | 'cancel_queued' | 'ping' | 'resolve_permission' | 'resolve_picker' | 'get_capabilities' | 'store_credential' | 'clear_credential' | 'clear_context' | 'read_task_output' | 'stop_task' | 'reload_skills' | 'app_tool_result';
  provider?: Provider;
  /**
   * For `type: 'send'`: turnId generated by main side so it can pre-register
   * the per-turn event dispatcher before we start emitting. Missing turnId
   * falls back to a freshly-generated one — defensive only; main always sets
   * it in the current architecture.
   */
  turnId?: string;
  prompt?: string;
  cwd?: string;
  resume?: string;
  permissionMode?: string;
  model?: string;
  effort?: string;
  images?: string[];
  sessionId?: string;
  /** For `type: 'send'`: a structured config edit (picker / status-bar). When
   *  present, the provider applies it + emits a divider instead of running a
   *  query — prompt is empty for these turns. */
  configEdit?: { key: 'model' | 'effort' | 'permissionMode'; value: string };
  /** For `type: 'send'` / `type: 'cancel_queued'`: renderer-minted correlation
   *  key. Tracked through the send queue + echoed in the queue snapshot. */
  clientMsgId?: string;
  toolUseId?: string;
  allow?: boolean;
  /** For `type: 'ping'`: client heartbeat sequence, echoed back in pong for RTT. */
  seq?: number;
  /** For `type: 'send'`: app-instance id — names this app's projected skills dir
   *  at `~/.shelf/apps/<appId>/skills` (see deployment#1). */
  appId?: string;
  /** resolve_picker payload — picker id minted by provider, payload carries
   * index-aligned answers or { cancelled: true } (see PickerResolvePayload). */
  pickerId?: string;
  payload?: PickerResolvePayload;
  message?: string;
  requestId?: string;
  /** For `type: 'app_tool_result'`: main's reply to an app_tool request. */
  ok?: boolean;
  data?: unknown;
  error?: string;
  key?: string;
  customModels?: ProviderModel[];
  scope?: 'once' | 'session';
  /** For `type: 'read_task_output'`: which background task's remote output_file to read. */
  taskId?: string;
  /**
   * For `type: 'get_capabilities'`: renderer's saved prefs forwarded so
   * session-stateful providers (Copilot) can seed currentModel/currentEffort/
   * currentPermissionMode closures before reporting `current*` back.
   */
  intent?: { model?: string; effort?: string; permissionMode?: string };
}

function send(msg: OutgoingMessage) {
  // Wire-TX diagnostic trace (debug level → off by default; flip logLevel=debug
  // to capture). Proves which events the provider actually emitted over stdout —
  // the top of the chain when tracing a sleep/network wedge where tool results
  // never reach the renderer. Skip `type:'log'` to avoid recursion (serverLog
  // routes back through this same send) and log spam. See connection-wedge trace.
  const m = msg as unknown as Record<string, unknown>;
  if (m.type !== 'log') {
    serverLog('debug', 'wire-tx', wireSummary(m));
  }
  process.stdout.write(JSON.stringify(msg) + '\n');
}

/** Compact one-line summary of a wire message for the wire-tx/wire-rx traces. */
function wireSummary(m: Record<string, unknown>): string {
  const bit = (k: string, v: unknown) => (v == null ? '' : ` ${k}=${String(v).slice(0, 12)}`);
  return `type=${String(m.type)}${bit('turn', m.turnId)}${bit('msgType', m.msgType)}${bit('msgId', m.msgId)}`
    + (typeof m.content === 'string' ? ` len=${m.content.length}` : '');
}

// Route all agent-server diagnostics back to main over the wire (see
// server-logger.ts). After this, serverLog() no longer falls back to stderr.
setLogSink(send);

// Last appId seen on a `send` — names this app's projected skills dir; used to
// keep its `.heartbeat` lease fresh + protect it from the startup sweep.
let lastAppId: string | undefined;

/**
 * Refresh the `.heartbeat` leases the cleanup sweep reads (see #70 / §5.9):
 * the version dir (when running under `~/.shelf/agent-server/<version>/`; local
 * dist runs skip it) and this app's `~/.shelf/apps/<appId>/` dir. Best-effort —
 * never break the pong reply.
 */
function touchHeartbeats(): void {
  try {
    if (/\.shelf\/agent-server\//.test(__dirname)) {
      fs.writeFileSync(path.join(__dirname, '.heartbeat'), '');
    }
  } catch { /* version lease best-effort */ }
  try {
    if (lastAppId) {
      const appDir = path.join(os.homedir(), '.shelf', 'apps', lastAppId);
      fs.mkdirSync(appDir, { recursive: true });
      fs.writeFileSync(path.join(appDir, '.heartbeat'), '');
    }
  } catch { /* app lease best-effort */ }
}

const backends = new Map<Provider, ServerBackend>();
let activeBackend: ServerBackend | null = null;

/**
 * Per-session pref cache. Orchestrator drives "did renderer change pref X
 * since last send?" diff detection — calls backend.setModel/setEffort/
 * setPermissionMode only on change. Keyed by sessionId so multi-tab
 * scenarios don't cross-contaminate each other's diff state (which the
 * old per-provider-singleton closure cache in Copilot did suffer from).
 */
const lastAppliedPrefs = new Map<string, { model?: string; effort?: string; permissionMode?: string }>();

async function applyPrefDiff(
  backend: ServerBackend,
  sessionKey: string,
  incoming: { model?: string; effort?: string; permissionMode?: string },
): Promise<void> {
  // Cache update is gated on apply success — a failed setX must not record
  // the requested value as "applied" or the next turn's diff would skip the
  // retry (silently leaving the SDK on the prior value while the cache
  // pretends the switch happened).
  const last = lastAppliedPrefs.get(sessionKey) ?? {};
  const next = { ...last };
  if (incoming.model !== undefined && incoming.model !== last.model) {
    try {
      await backend.setModel?.(incoming.model);
      next.model = incoming.model;
    } catch (err: any) {
      send({ type: 'error', error: `Failed to switch model to "${incoming.model}": ${err?.message ?? err}` });
    }
  }
  if (incoming.effort !== undefined && incoming.effort !== last.effort) {
    try {
      await backend.setEffort?.(incoming.effort);
      next.effort = incoming.effort;
    } catch (err: any) {
      send({ type: 'error', error: `Failed to set effort "${incoming.effort}": ${err?.message ?? err}` });
    }
  }
  if (incoming.permissionMode !== undefined && incoming.permissionMode !== last.permissionMode) {
    try {
      await backend.setPermissionMode?.(incoming.permissionMode);
      next.permissionMode = incoming.permissionMode;
    } catch (err: any) {
      send({ type: 'error', error: `Failed to set permission mode "${incoming.permissionMode}": ${err?.message ?? err}` });
    }
  }
  lastAppliedPrefs.set(sessionKey, next);
}

/**
 * Test-mode override: when SHELF_TEST_MODE=1, every provider lookup returns
 * the same fake backend regardless of requested provider name. E2E specs
 * exercise renderer + main + agent-server wire chain without spinning up
 * real Claude/Copilot SDKs. Production builds set the env to undefined so
 * this branch is dead. See agent-core#5.
 */
const TEST_MODE = process.env.SHELF_TEST_MODE === '1';

function getBackend(provider: Provider): ServerBackend {
  const key = (TEST_MODE ? 'fake' : provider) as Provider;
  let b = backends.get(key);
  if (b) return b;
  if (TEST_MODE) {
    b = createFakeBackend();
  } else {
    switch (provider) {
      case 'claude':
        b = createClaudeBackend();
        break;
      case 'copilot':
        b = createCopilotBackend();
        break;
    }
  }
  backends.set(key, b);
  return b;
}

async function handleSend(msg: IncomingMessage) {
  if (msg.appId) lastAppId = msg.appId;
  const turnId = msg.turnId ?? newTurnId();
  const turnSend = wrapSendForTurn(turnId, send);

  // A config-edit turn carries a structured edit instead of a prompt.
  const isConfigEdit = !!msg.configEdit;
  // An image-only turn has an empty prompt but DOES carry images — it's valid.
  // Every early-return below MUST emit `idle` (not just an error): the renderer
  // flipped to streaming on send, and without a terminal idle its spinner +
  // queue-flush latch wedge forever (the "whole conversation stuck" bug when an
  // image was sent with no text).
  const hasInput = !!msg.prompt || (msg.images?.length ?? 0) > 0;
  if (!isConfigEdit && (!hasInput || !msg.cwd)) {
    turnSend({ type: 'error', error: !msg.cwd ? 'Missing cwd' : 'Missing prompt or images' });
    turnSend({ type: 'status', state: 'idle' });
    return;
  }
  if (isConfigEdit && !msg.cwd) {
    turnSend({ type: 'error', error: 'Missing cwd' });
    turnSend({ type: 'status', state: 'idle' });
    return;
  }
  const provider = msg.provider ?? 'claude';
  let backend: ServerBackend;
  try {
    backend = getBackend(provider);
  } catch (err: any) {
    turnSend({ type: 'error', error: err.message });
    turnSend({ type: 'status', state: 'idle' });
    return;
  }
  activeBackend = backend;

  // Hydrate persisted context once per turn — providers read fields they care
  // about (e.g. `lastSdkSessionId`) without touching disk themselves.
  const restoreContext = loadRestoreContextFor(provider, msg.sessionId);

  // Pref diff only for prompt turns. A config-edit turn IS the change — it's
  // applied by the provider via input.configEdit; running the diff too would
  // double-apply (and emit a redundant capabilities).
  if (!isConfigEdit) {
    // Renderer-authoritative model/effort/permissionMode arrive in every send
    // payload. Orchestrator compares against last-applied for this session and
    // calls provider.setX? only on change. Per-sessionId so multi-tab doesn't
    // cross-contaminate.
    await applyPrefDiff(backend, msg.sessionId ?? '', {
      model: msg.model,
      effort: msg.effort,
      permissionMode: msg.permissionMode,
    });
  }

  const input: QueryInput = {
    prompt: msg.prompt ?? '',
    cwd: msg.cwd!,
    resume: msg.resume,
    permissionMode: msg.permissionMode,
    model: msg.model,
    effort: msg.effort,
    images: msg.images,
    sessionId: msg.sessionId,
    configEdit: msg.configEdit,
    clientMsgId: msg.clientMsgId,
    appId: msg.appId,
    restoreContext,
  };

  const contextAware = wrapSendForContext(provider, msg.sessionId, turnSend);
  await backend.query(input, contextAware);
}

async function handleStop() {
  if (activeBackend) {
    await activeBackend.stop();
  }
}

// ── Idle-shutdown watchdog (ssh only) ────────────────────────────────────────
// `--idle-shutdown-min=N` is passed ONLY by the ssh spawn path (remote.ts): the
// remote host is not fate-shared with the client, so it keeps running while the
// laptop sleeps / app closes. When no `ping` arrives for N minutes, self-exit to
// free remote resources; the client respawns + resumes on reconnect. Fate-shared
// transports (local/docker/wsl) suspend with the host and never pass this arg.
// N is parsed as a float so tests can use sub-minute values. See connection-health#2.
const idleArg = process.argv.find((a) => a.startsWith('--idle-shutdown-min='));
const IDLE_SHUTDOWN_MS = idleArg ? Math.max(0, Number(idleArg.split('=')[1]) || 0) * 60_000 : 0;
let watchdogTimer: NodeJS.Timeout | undefined;
function resetWatchdog() {
  if (!IDLE_SHUTDOWN_MS) return;
  if (watchdogTimer) clearTimeout(watchdogTimer);
  watchdogTimer = setTimeout(() => {
    // Death path → stderr (NOT serverLog): main stopped pinging (likely gone), so a
    // wire log wouldn't deliver/flush anyway.
    console.error(`[agent-server] idle-shutdown: no ping for ${IDLE_SHUTDOWN_MS / 60_000}min — self-exiting`);
    // This IS a normal closure (a disconnect) — agent-server is alive, so run the
    // full shutdown (reap escaped detached tasks → dispose → exit), not a bare exit.
    void shutdown();
  }, IDLE_SHUTDOWN_MS);
}
resetWatchdog(); // arm from boot — if the client never pings, it's already gone

// Wire the app-tool bridge sender so in-process bridge tools can callMain().
initAppToolClient(send);

const rl = readline.createInterface({ input: process.stdin, terminal: false });

// ── Server-owned send queue ──────────────────────────────────────────────────
// The client (renderer) eager-sends every submission; THIS side owns the timing
// (排序 + 釋放時機). The queue serializes turns one-at-a-time (concurrent turn
// handlers would race on the provider backend's module-level state — currentSend
// / abortController / activeQuery / lastSessionId) AND is introspectable so we
// emit a snapshot the renderer mirrors. Logic lives in the pure `createSendQueue`
// factory (send-queue.ts, unit-tested). See message-queue-ownership design.
const sendQueue = createSendQueue<IncomingMessage>({
  handle: (msg) => handleSend(msg),
  // Session-level (no turnId) so main routes it to the `onQueue` sink.
  emitSnapshot: (items) => send({ type: 'queue', items }),
  // Dropped queued send → terminal idle on its turnId, or main's per-turn
  // generator (registered before the send was sent) hangs forever.
  terminateTurn: (turnId) => send({ type: 'status', state: 'idle', turnId } as OutgoingMessage),
  onHandleError: (msg, err) => {
    const tid = msg.turnId ?? '';
    send({ type: 'error', error: (err as any)?.message ?? String(err), turnId: tid } as OutgoingMessage);
    send({ type: 'status', state: 'idle', turnId: tid } as OutgoingMessage);
  },
  // Never silent: a cancel that didn't cleanly remove a queued item. 'cancel-
  // running' is a benign race (snapshot re-promotes); 'cancel-unknown' is a real
  // desync worth investigating → warn, routed to main's logger over the wire.
  onAnomaly: (reason, clientMsgId) => {
    serverLog('warn', 'send-queue', `${reason} clientMsgId=${clientMsgId}`);
  },
});

rl.on('line', (line) => {
  let msg: IncomingMessage;
  try {
    msg = JSON.parse(line);
  } catch {
    send({ type: 'error', error: 'Invalid JSON' });
    return;
  }

  switch (msg.type) {
    case 'send':
      sendQueue.enqueue(msg);
      break;
    case 'cancel_queued':
      if (typeof msg.clientMsgId === 'string') sendQueue.cancel(msg.clientMsgId);
      break;
    case 'stop':
      // ESC: clear the waiting queue (server is the queue authority now) +
      // interrupt the running turn. clear() emits the updated snapshot.
      sendQueue.clear();
      handleStop();
      break;
    case 'ping':
      // Heartbeat: echo `seq` (client measures RTT on its own clock), refresh the
      // version-dir liveness lease the cleanup sweep reads (§5.9), and reset the
      // ssh idle-shutdown watchdog (the client is alive).
      send({ type: 'pong', seq: msg.seq });
      touchHeartbeats();
      resetWatchdog();
      break;
    case 'resolve_permission':
      if (activeBackend && msg.toolUseId !== undefined) {
        activeBackend.resolvePermission?.(msg.toolUseId, msg.allow ?? false, msg.message, msg.scope);
      }
      break;
    case 'resolve_picker':
      if (activeBackend && msg.pickerId !== undefined && msg.payload !== undefined) {
        activeBackend.resolvePicker?.(msg.pickerId, msg.payload);
      }
      break;
    case 'app_tool_result':
      // main's reply to an app_tool request — resolve the awaiting bridge tool.
      if (typeof msg.requestId === 'string') {
        resolveAppToolResult(msg.requestId, { ok: msg.ok ?? false, data: msg.data, error: msg.error });
      }
      break;
    case 'stop_task':
      // Fire-and-forget: the provider stops the task; the resulting
      // task_notification('stopped') flows back via the task_event lane.
      if (activeBackend && msg.taskId !== undefined) {
        void activeBackend.stopTask?.(msg.taskId);
      }
      break;
    case 'get_capabilities': {
      const provider = msg.provider ?? 'claude';
      (async () => {
        try {
          const backend = getBackend(provider);
          const caps = await backend.gatherCapabilities?.(msg.cwd ?? process.cwd(), msg.sessionId, msg.customModels, msg.intent);
          send({ type: 'capabilities', requestId: msg.requestId ?? '', ...(caps ?? {}) });
        } catch (err: any) {
          // Real data loss: gatherCapabilities threw → response carries only
          // `error`, and remote.ts resolves an all-empty caps (perm modes vanish).
          // Loud so the true cause (e.g. Copilot listModels network throw) is visible.
          serverLog('error', 'caps', `${provider} gatherCapabilities threw: ${err?.message ?? err} reqId=${msg.requestId ?? ''}`);
          send({ type: 'capabilities', requestId: msg.requestId ?? '', error: err?.message ?? String(err) });
        }
      })();
      break;
    }
    case 'store_credential': {
      const provider = msg.provider ?? 'claude';
      (async () => {
        try {
          const backend = getBackend(provider);
          if (!backend.storeCredential) throw new Error(`Provider ${provider} does not accept API keys`);
          await backend.storeCredential(msg.key ?? '');
          send({ type: 'credential_stored', requestId: msg.requestId ?? '', ok: true });
        } catch (err: any) {
          send({ type: 'credential_stored', requestId: msg.requestId ?? '', ok: false, error: err?.message ?? String(err) });
        }
      })();
      break;
    }
    case 'clear_credential': {
      const provider = msg.provider ?? 'claude';
      (async () => {
        try {
          const backend = getBackend(provider);
          if (!backend.clearCredential) throw new Error(`Provider ${provider} has no credential to clear`);
          await backend.clearCredential();
          send({ type: 'credential_cleared', requestId: msg.requestId ?? '', ok: true });
        } catch (err: any) {
          send({ type: 'credential_cleared', requestId: msg.requestId ?? '', ok: false, error: err?.message ?? String(err) });
        }
      })();
      break;
    }
    case 'read_task_output': {
      const provider = msg.provider ?? 'claude';
      (async () => {
        try {
          const backend = getBackend(provider);
          if (!backend.readTaskOutput) throw new Error(`Provider ${provider} does not support reading task output`);
          const content = await backend.readTaskOutput(msg.taskId ?? '');
          send({ type: 'task_output', requestId: msg.requestId ?? '', content });
        } catch (err: any) {
          send({ type: 'task_output', requestId: msg.requestId ?? '', error: err?.message ?? String(err) });
        }
      })();
      break;
    }
    case 'reload_skills':
      // App-level skills changed on disk (main already projected/synced them to
      // the consumption path). App skills are app-global, not per-session, so
      // tell EVERY instantiated provider to re-scan on its live session. Each is
      // best-effort + no-op without a live session. Effect lands next turn.
      // Emit a `skills_reloaded` result (system/error line in the agent view)
      // via the BASE send — turnId-less by construction. Skip no-op reloads.
      for (const b of backends.values()) {
        void b.reloadSkills?.().then((r) => {
          if (r && r.reloaded) send({ type: 'skills_reloaded', ok: r.ok, ...(r.error ? { error: r.error } : {}) });
        });
      }
      break;
    case 'clear_context': {
      if (msg.sessionId) {
        deleteContext(msg.sessionId);
        // Tell every provider that has cached state for this session to drop it,
        // so they don't try to resume from a now-deleted lastSdkSessionId on
        // the next turn. Sync per-backend call — they just clear in-memory refs.
        for (const b of backends.values()) b.resetSession?.(msg.sessionId);
      }
      break;
    }
  }
});

// Every NORMAL agent-server closure funnels through shutdown(): reap escaped
// detached tasks, dispose backends, exit. Called from BOTH the stdin-close handler
// and the idle watchdog; `shuttingDown` makes it idempotent (both can fire). It
// ALWAYS reaps — a closure while we're alive is a real end of session (no
// reconnect). Only an ABNORMAL closure (agent-server itself died) skips this →
// crash net (Phase 2). Reap timeout is bounded so a hung provider RPC can't stall.
let shuttingDown = false;
const REAP_TIMEOUT_MS = 2000;

async function shutdown(): Promise<void> {
  if (shuttingDown) return;
  shuttingDown = true;
  await performShutdown({
    backends: [...backends.values()],
    reapTimeoutMs: REAP_TIMEOUT_MS,
    log: (level, msg) => serverLog(level, 'reaper', msg),
    exit: () => process.exit(0),
  });
}

rl.on('close', () => {
  void shutdown();
});

cleanupOldContexts();
// Reclaim stale ~/.shelf dirs via heartbeat-lease (replaces the old eager
// "delete every other version" cleanup that thrashed on shared remotes — see
// #70 / §5.9). currentVersion = our own deploy dir name. lastAppId is unknown
// until the first send, so the apps sweep keeps any fresh-lease dir this pass.
runCleanupSweep(path.basename(__dirname), lastAppId);
send({ type: 'ready' });
